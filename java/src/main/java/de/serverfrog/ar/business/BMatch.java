package de.serverfrog.ar.business;

import de.serverfrog.ar.eao.MatchRepository;
import de.serverfrog.ar.entity.Match;
import lombok.RequiredArgsConstructor;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Scope;
import org.springframework.stereotype.Component;

import javax.transaction.Transactional;
import java.util.ArrayList;
import java.util.List;

import static org.springframework.beans.factory.config.BeanDefinition.SCOPE_PROTOTYPE;

@Component
@Scope(SCOPE_PROTOTYPE)
@RequiredArgsConstructor(onConstructor = @__(@Autowired))
public class BMatch {

    private final MatchRepository matchRepository;

    @Transactional
    public List<Match> findAllMatchesEager() {
        Iterable<Match> all = matchRepository.findAll();
        List<Match> loaded = new ArrayList<>();

        for (Match m : all) {
            m.fetch();
            loaded.add(m);
        }
        return loaded;
    }

    @Transactional
    public List<Match> findByOutcomeEager(Match.Outcome outcome) {

        List<Match> byOutcome = matchRepository.findByOutcome(outcome);
        byOutcome.forEach(Match::fetch);
        return byOutcome;
    }

}
